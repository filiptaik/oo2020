
public class PrimitiveTypes {
    public static void main(String[] args) {

        // 8-bit, numbrid -128 - 127
        byte b = 10;

        //16-bit, -32,768 - 32,767
        short s = 500;

        //32-bit, -2,147,483,648 - 2,147,483,647
        int i =  5334413;

        //64-bit,  -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        long l = 70000000000000L;

        //32-bit, 6 - 7 komakohta
        float f = 34.6f;

        //64-bit, kuni 15 komakohta
        double d = 7.761594925;

        //16-bit, üksik täht või ASCII väärtus
        char c = 's';
        char x = 72;

        //1-bit, true või false väärtused
        boolean bool = true;
    
    }
}